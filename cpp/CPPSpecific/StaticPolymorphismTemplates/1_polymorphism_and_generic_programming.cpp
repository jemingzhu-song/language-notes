#include <iostream>

/*
 * Polymorphism & Generic Programming
 *
 * • Polymorphism: Provision of a single interface to entities of different types. Two types
 *   of Polymorphism:
 *          1. Static:
 *              • Function Overloading
 *              • Templates (i.e. Generic Programming)
 *                  e.g. std::vector<int>
 *                  e.g. std::vector<std::string>
 *          2. Dynamic
 *              • Virtual Functions
 *              • Inheritance
 * • Generic Programming: Generalising software components to be independent of a particular
 *   type.
 *      • STL is a great example of generic programming
*/